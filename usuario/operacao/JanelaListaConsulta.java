    public JanelaListaConsulta(ServidorClinica fachada){
        this.fachada = fachada;
        initComponents();
        setModal(true);
        DefaultTableModel tabela = (DefaultTableModel)this.jTable1.getModel();
        ArrayList<Consulta> consultas = fachada.getConsultas();
        String dataString;
        SimpleDateFormat format;
        tabela.setRowCount(consultas.size());
        for(int i=0; i< consultas.size(); i++){
           tabela.setValueAt(consultas.get(i).getCodigo(), i, 0);
           tabela.setValueAt(consultas.get(i).getPaciente().getNome(), i, 1);
           tabela.setValueAt(consultas.get(i).getMedico().getNome(), i, 2);
           format = new SimpleDateFormat("dd/MM/yyyy");
           dataString = format.format(consultas.get(i).getData());
           tabela.setValueAt(dataString, i, 3);
        }
    }

